    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
         pos[conta] = Integer.parseInt(txtnum.getText());    
         ex1[conta]= Integer.parseInt(txtex1.getText());    
         ex2[conta]= Integer.parseInt(txtex2.getText());    
         ex3[conta]= Integer.parseInt(txtex3.getText());    
         conta++;   
         txtnum.setText("");    
         txtex1.setText("");   
         txtex2.setText("");    
         txtex3.setText("");    
         txtnum.requestFocus();
    }//GEN-LAST:event_btngrabarActionPerformed

    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
       double suma=0;     
       for(int i=0;i<conta;i++) {        
           prom[i]=((ex1[i]+ex2[i]+ex3[i])/3);        
          suma=suma+ex3[i];    
        }     
       double promex3=suma/conta;    
       txtpromex3.setText(""+promex3); 
       
        double mayor=0;    
        for(int i=0;i<conta;i++){       
            if(prom[i]>mayor){            
                mayor=prom[i];
                x=i; 
            }            
             if(prom[i]>10.5)             
                 contap++;        
             else             
                 contd++; 
             if(ex1[i]<10.5)           
                 contex1++;   
        }  
      txtmayor.setText(""+mayor);     
      txtestmayor.setText(""+pos[x]);    
      txtaprob.setText(""+contap);     
      txtdesaprob.setText(""+contd);     
      txtdesaprob1.setText(""+contex1);   
    }//GEN-LAST:event_btncalcularActionPerformed

